namespace UnityCraft.Generation
{
    public enum GenerationTheme
    {
        /// <summary>
        /// Regular world with Day/Night cycle.
        /// </summary>
        Normal = 0,

        /// <summary>
        /// Ocean is replaced with lava, permament night.
        /// </summary>
        Hell = 1,

        /// <summary>
        /// Permament day.
        /// </summary>
        Paradise = 2,

        /// <summary>
        /// Permament cloudy weather, monsters will spawn in the overworld.
        /// </summary>
        Woods = 3
    }
}